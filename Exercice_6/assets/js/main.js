function inputBorder() {
    let firstName = document.getElementById('firstName');
    let lastName = document.getElementById('lastName');
    let age = document.getElementById('age');

    firstName.style.border = 'black 2px solid';
    lastName.style.border = 'black 2px solid';
    age.style.border = 'black 2px solid';
}

function inputBorderLess() {
    let firstName = document.getElementById('firstName');
    let lastName = document.getElementById('lastName');
    let age = document.getElementById('age');

    firstName.style.border = 'blue 2px solid';
    lastName.style.border = 'blue 2px solid';
    age.style.border = 'blue 2px solid';
}